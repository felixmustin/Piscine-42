#include <unistd.h>

int	ft_atoi(char *str)
{
	int	n;
	int	neg;

	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	neg = 0;
	while (*str == '-' || *str == '+')
	{
		if (*str++ == '-')
		{
			neg = 1 - neg;
		}
	}
	n = 0;
	while (*str >= '0' && *str <= '9')
	{
		n = n * 10 + *str++ - '0';
	}
	if (neg)
	{
		n = n * -1;
	}
	return (n);
}
