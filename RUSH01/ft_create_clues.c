#include <unistd.h>

void	ft_create_clues(int clues[4][4], char *str)
{
	int	i;
	int	a;
	int	b;

	a = 0;
	b = 0;
	i = 0;
	while (str[i] != '\0')
	{
		clues[a][b] = str[i] - 48;
		b++;
		if (b == 4)
		{
			b = 0;
			a++;
		}
		if (str[i + 1] == '\0')
		{
			i--;
		}
		i += 2;
	}
}
