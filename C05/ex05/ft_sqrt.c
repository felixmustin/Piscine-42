#include <unistd.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb < 1)
	{
		return (0);
	}
	else
	{
		while (nb / i > i)
		{
			i++;
		}
		if (i * i == nb)
			return (i);
		else
			return (0);
	}
}
