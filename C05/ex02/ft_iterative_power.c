#include <unistd.h>

int	ft_iterative_power(int nb, int power)
{
	int	rep;

	if (power < 0)
	{
		return (0);
	}
	else if (power == 0)
	{
		return (1);
	}
	else
	{
		rep = nb;
		while (--power != 0)
		{
			rep = rep * nb;
		}
		return (rep);
	}
}
